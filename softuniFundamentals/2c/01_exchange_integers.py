
# Read two integer numbers and, after that, exchange their values.
# Print the variable values before and after the exchange, as shown below:
# Before:
# a = 5
# b = 10
# After:
# a = 10
# b = 5

first_number = int(input())
second_number = int(input())

print("Before:")
print(f"a = {first_number}")
print(f"b = {second_number}")
print("After:")
print(f"a = {second_number}")
print(f"b = {first_number}")
